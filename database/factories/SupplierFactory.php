    {
        return [
            'name' => 'PT ' . $this->faker->company(),
            'email' => $this->faker->unique()->companyEmail(),
            'phone' => $this->faker->phoneNumber(),
            'address' => $this->faker->address(),
            'notes' => $this->faker->sentence(),
            'created_at' => now(),
        ];
    }
}
